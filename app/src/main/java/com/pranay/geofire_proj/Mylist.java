package com.pranay.geofire_proj;

public class Mylist
{
    String id;
    String nama;

    public Mylist(String id, String nama) {
        this.id = id;
        this.nama = nama;
    }

    public Mylist() {

    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getnama() {
        return nama;
    }

    public void setnama(String nama) {
        this.nama = nama;
    }
}
