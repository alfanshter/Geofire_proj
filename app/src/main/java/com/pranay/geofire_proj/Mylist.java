package com.pranay.geofire_proj;

public class Mylist
{
    String id;
    String name;

    public Mylist(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public Mylist() {

    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
